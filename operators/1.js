// Increment and Decrement Operators

a = a + 1;
a += 1; // => a = a + 1
a++;//Post increment
console.log(a); // The value of a is 10 after post increment it will be 11
console.log(a++);// if we do post increment in console.log it will print 10
console.log(a);//Now it will print 11(First it will print a and then do the post increment)

++a;//Pre increment
console.log(++a);// it will print 11

//Post Decrement
console.log(b--);
